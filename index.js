let outputEl = document.getElementById("output-el");

function getRandomPassword() {
  let length = document.getElementById("input-el").value;
  outputEl.textContent = "";
  for (let i = 0; i < length; i++) {
    let p = Math.floor(Math.random() * letters.length);
    outputEl.textContent += letters[p];
  }
}
